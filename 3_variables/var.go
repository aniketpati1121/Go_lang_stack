package main

import "fmt"

func main() {

	var name = "John"
	fmt.Println(name)

	var isAdult bool = true
	fmt.Println(isAdult)

	var age int = 30
	fmt.Println(age)

	// short variable declaration
	country := "USA"
	fmt.Println(country)

	const pi = 3.14
	fmt.Println(pi)

	name = "Doe"
	fmt.Println(name)

	// var price float64 = 9.99
	var price = 9.99
	fmt.Println(price)

}

