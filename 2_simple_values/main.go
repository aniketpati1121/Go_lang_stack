package main

import "fmt"

func main() {

	// simple values
	fmt.Println(1 + 1)
	fmt.Println(10 - 3)
	fmt.Println(4 * 2)
	fmt.Println(8 / 2)
	fmt.Println(7 % 3)

	//string
	fmt.Println("Helllo Pranav")

	//boolean
	fmt.Println(true)
	fmt.Println(false)

	//float
	fmt.Println(3.14)
	fmt.Println(1.1 + 2.2)
}

